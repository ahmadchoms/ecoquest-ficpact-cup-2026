// Get random exclusive items for daily refresh
export const getDailyExclusiveItems = (count = 4) => {
  const shuffled = [...exclusiveItems].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// Get items by category
export const getItemsByCategory = (category) => {
  if (category === "all") {
    return commonItems;
  }
  return commonItems.filter((item) => item.category === category);
};

